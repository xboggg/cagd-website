The TPRS is a system that allows Third Party Institutions (TPIs) to directly deduct loan repayments, Insurance premiums, welfare contributions and credit union deductions from salaries of Government of Ghana (GOG) employees at source.

USEFULNESS OF THE THIRD-PARTY REFERENCE SYSTEM (TPRS):
1. The upgrade is to address fraudulent and impersonation issues of GOG employees.
2. The upgraded TPRS will address wrongful deductions from TPIs.
3. It will also address affordability issues of GOG employees when applying for loans and other facilities.
4. It will resolve credit worthiness of GOG employees when applying for loans and other facilities.

NEW FEATURES OF THE TPRS:
The new features of the TPRS are the generation of a Mandate Pin in addition to the Mandate Number. An employee needs an automatically generated Mandate Number and Mandate Pin which will be sent to his/her phone for any transaction with a TPI. Without a Mandate Pin, a TPI cannot process any transaction on an employee.

ACCESSIBILITY OF TPI''S FACILITIES:
Employees are also required to upload their pictures on the Government of Ghana (GOG) E-Payslip platform to enable them access TPI''s facilities.

GUARANTOR''S SIGNATURE:
The upgraded system does not require a guarantor''s signature as a condition for an employee to access a facility. This makes the processing of facilities from TPIs faster.

